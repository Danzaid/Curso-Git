public class Industrias {
    private String industria;
    private String descripcion;
    private String codigoIndustria;
    private String tipo;
    private String codigoIdioma;
    private String propietarioGlobal;
    private String rowId;

    public String getRowId() {
        return rowId;
    }

    public void setRowId(String rowId) {
        this.rowId = rowId;
    }

    public String getIndustria() {
        return industria;
    }

    public void setIndustria(String industria) {
        this.industria = industria;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCodigoIndustria() {
        return codigoIndustria;
    }

    public void setCodigoIndustria(String codigoIndustria) {
        this.codigoIndustria = codigoIndustria;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCodigoIdioma() {
        return codigoIdioma;
    }

    public void setCodigoIdioma(String codigoIdioma) {
        this.codigoIdioma = codigoIdioma;
    }

    public String getPropietarioGlobal() {
        return propietarioGlobal;
    }

    public void setPropietarioGlobal(String propietarioGlobal) {
        this.propietarioGlobal = propietarioGlobal;
    }
    
}//end
