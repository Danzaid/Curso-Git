public class ListaPolizasPadre {//corregido
    private String nombre;
    private String objetoFlujoTrabajo;
    private String grupoPolizas;
    private String duracion;
    private String unidades;
    private String rowId;   

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getObjetoFlujoTrabajo() {
        return objetoFlujoTrabajo;
    }

    public void setObjetoFlujoTrabajo(String objetoFlujoTrabajo) {
        this.objetoFlujoTrabajo = objetoFlujoTrabajo;
    }

    public String getGrupoPolizas() {
        return grupoPolizas;
    }

    public void setGrupoPolizas(String grupoPolizas) {
        this.grupoPolizas = grupoPolizas;
    }

    public String getDuracion() {
        return duracion;
    }

    public void setDuracion(String duracion) {
        this.duracion = duracion;
    }

    public String getUnidades() {
        return unidades;
    }

    public void setUnidades(String unidades) {
        this.unidades = unidades;
    }

    public String getRowId() {
        return rowId;
    }

    public void setRowId(String rowId) {
        this.rowId = rowId;
    }

   

    
}
