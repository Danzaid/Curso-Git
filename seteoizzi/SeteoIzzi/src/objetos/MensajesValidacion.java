public class MensajesValidacion {//corregido        
    private String codigoMensaje;
    private String nivelMensaje;
    private String origenMensaje;
    private String textoMensaje;
    private String descripcion;
    private String rowId;  

    public String getRowId() {
        return rowId;
    }

    public void setRowId(String rowId) {
        this.rowId = rowId;
    }

    public String getCodigoMensaje() {
        return codigoMensaje;
    }

    public void setCodigoMensaje(String codigoMensaje) {
        this.codigoMensaje = codigoMensaje;
    }

    public String getNivelMensaje() {
        return nivelMensaje;
    }

    public void setNivelMensaje(String nivelMensaje) {
        this.nivelMensaje = nivelMensaje;
    }

    public String getOrigenMensaje() {
        return origenMensaje;
    }

    public void setOrigenMensaje(String origenMensaje) {
        this.origenMensaje = origenMensaje;
    }

    public String getTextoMensaje() {
        return textoMensaje;
    }

    public void setTextoMensaje(String textoMensaje) {
        this.textoMensaje = textoMensaje;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    

    
}
